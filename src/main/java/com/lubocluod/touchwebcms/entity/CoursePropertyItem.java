package com.lubocluod.touchwebcms.entity;

public class CoursePropertyItem {
    int propItemId;
    int propGroupId;
    String propItemName;

    public int getPropItemId() {
        return propItemId;
    }

    public void setPropItemId(int propItemId) {
        this.propItemId = propItemId;
    }

    public int getPropGroupId() {
        return propGroupId;
    }

    public void setPropGroupId(int propGroupId) {
        this.propGroupId = propGroupId;
    }

    public String getPropItemName() {
        return propItemName;
    }

    public void setPropItemName(String propItemName) {
        this.propItemName = propItemName;
    }
}
